using namespace std;
using ll = long long;

void solve() {
    int n;
    cin >> n;
    for (int i = n; i >= 1; i--) {
        cout << i << (i == 1 ? '\n' : ' ');
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) solve();
    return 0;
}