using namespace std;
using ll = long long;

void solve() {
    int n;cin >> n;
    ll mx = 0;

    for (int i=0;i<n;i++) {
        ll x;cin >> x;
        if (mx>x) mx+=x;
        else mx = x;
    }

    cout << mx << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) solve();
    return 0;
}