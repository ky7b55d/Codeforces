using namespace std;
using ll = long long;

void solve() {
    int n;
    cin >> n;

    ll sum = 0;
    ll ans = (ll)4e18;

    for (int i = 1; i <= n; i++) {
        ll x;
        cin >> x;
        sum += x;
        ans = min(ans, sum / i);
        cout << ans << " ";
    }
    cout << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) solve();
    return 0;
}