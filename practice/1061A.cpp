using namespace std;
using ll = long long;

void solve() {
    int n; cin >> n;

    int remain=n;
    int haoeat=0;
    while (remain>=3){
        haoeat+=(remain/3);
        remain=remain-(remain/3)*2;
    }
    cout << haoeat << "\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) solve();
    return 0;
}